
// function for meter tag.
function increaseMeter(){
    var i = document.getElementById("mtr");
    i.value+=10;
}

function decreaseMeter(){
    var i = document.getElementById("mtr");
    i.value-=10;
}


// function for progress tag

function incr(){
    var x = document.getElementById("file-progress");
    x.value+=10;
}
function decr(){
    var x = document.getElementById("file-progress");
    x.value-=10;
}


